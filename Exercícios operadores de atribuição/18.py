# Leia valor (float). Faça: += 5, depois /= 2, depois **= 2.
valor = float(input("Digite valor: "))

valor += 5
print("Este é o valor digitado somado a 5: ",valor)

valor /= 2
print("Este é o valor anterior divido por 2: ", valor)

valor **= 2
print("Este é o valor anterior multiplicado por ele mesmo: ", valor)
