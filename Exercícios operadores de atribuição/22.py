# Leia n (int). Faça n += 3, n *= 2, n -= 4, n //= 3. Mostre cada etapa usando a função print().

n = int(input("Digite o valor de n: "))
n += 3
print("O valor de n somado a 3 é: ", n)

n *= 2
print("O valor de n multiplcado por 2 é: ", n)

n -= 4
print("O valor de n subtraído 4 é: ", n)

n //= 3
print("O valor de n divido por 3 é: ", n)