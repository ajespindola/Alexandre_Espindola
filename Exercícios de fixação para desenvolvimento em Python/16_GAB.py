# Lê o primeiro número inteiro
a = int(input("Digite o valor de a: "))

# Lê o segundo número inteiro
b = int(input("Digite o valor de b: "))

# Calcula a soma
soma = a + b

# Calcula a subtração
subtracao = a - b

# Calcula a multiplicação
multiplicacao = a * b

# Exibe os três resultados no formato pedido
print("a + b =", soma, "| a - b =", subtracao, "| a * b =", multiplicacao)